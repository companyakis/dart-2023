void main() {
  var dayNum = 23;

  switch (dayNum) {
    case 1:
      print("Monday");
      break;
    case 2:
      print("Tuesday");
      break;
    case 3:
      print("Wednesday");
      break;
    case 4:
      print("Thursday");
      break;
    case 5:
      print("Friday");
      break;
    case 6:
      print("Saturday");
      break;
    case 7:
      print("Sunday");
      break;
    default:
      print("Select one of these numbers: 1, 2, 3, 4, 5, 6, 7!");
      break;
  }
}
